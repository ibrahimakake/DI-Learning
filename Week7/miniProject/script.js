    let div = document.getElementById("allCards")

    function appendCards(cards){
      div.innerHTML = ""
      cards.forEach((robot) => {
          let div1 = document.createElement("div")
          let image = document.createElement("img")
          image.src = robot.image
          div1.appendChild(image)
          let  div2 = document.createElement("div")
          let h3 = document.createElement("h3")
          h3.innerHTML=robot.name
          div2.appendChild(h3)
          let p = document.createElement("p")
          p.innerHTML= robot.email
          div2.appendChild(p)
          div1.appendChild(div2)
          div.appendChild(div1)
      });
    }

    appendCards(robots)

   let input = document.getElementById("input")
   input.addEventListener("input",Filter)
   
   function Filter(e){
    
    var filtredCards = robots.filter((value)=> {
      return value.name.toLowerCase().includes(e.target.value.toLowerCase())
    })

    appendCards(filtredCards)

   }
    
   
    
    
    
   
   